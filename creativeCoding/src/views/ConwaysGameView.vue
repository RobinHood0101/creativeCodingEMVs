                            // Alive cell
                            p.fill(100);
                            p.square(x, y, 20);
                            indexX++;
                        } else {
                            // Dead cell
                            p.fill(255);
                            p.square(x, y, 20);
                            indexX++;
                        }

                        // Check
                        let currentCell = nextGame[indexY][indexX];

                        let neighbour1 = this.getCell(indexY - 1, indexX - 1, this.currentGame);
                        let neighbour2 = this.getCell(indexY - 1, indexX, this.currentGame);
