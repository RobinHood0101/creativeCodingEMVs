                            if (currentCell === 1) {
                                nextGame[indexY][indexX] = 1;
                            }
                        }

                        // Check 3:
                        if (neighbourCount > 3) {
                            nextGame[indexY][indexX] = 0;
                        }

                        // Check 4:
                        if (neighbourCount === 3 && currentCell === 0) {
                            nextGame[indexY][indexX] = 1;
                        }
                    }
